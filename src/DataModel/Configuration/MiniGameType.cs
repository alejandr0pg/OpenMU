namespace MUnique.OpenMU.DataModel.Configuration;

/// <summary>
/// Defines the type of a mini game.
/// </summary>
public enum MiniGameType
{
    /// <summary>
    /// Undefined mini game type.
    /// </summary>
    Undefined,

    /// <summary>
    /// The devil square event.
    /// </summary>
    DevilSquare,

    /// <summary>
    /// The blood castle event.
    /// </summary>
    BloodCastle,

    /// <summary>
    /// The chaos castle event.
    /// </summary>
    ChaosCastle,

    /// <summary>
    /// The illusion temple event.
    /// </summary>
    IllusionTemple,

    /// <summary>
    /// The doppelganger event.
    /// </summary>
    Doppelganger,

    /// <summary>
    /// The imperial guardian (fortress) event.
    /// </summary>
    ImperialGuardian,

    /// <summary>
    /// The raklion boss event.
    /// </summary>
    Raklion,

    /// <summary>
    /// The crywolf defense event.
    /// </summary>
    Crywolf,

    /// <summary>
    /// The kanturu boss event.
    /// </summary>
    Kanturu,

    /// <summary>
    /// The castle siege event.
    /// </summary>
    CastleSiege,
}